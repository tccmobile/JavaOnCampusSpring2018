public class incDecDemo {

    public static void main(String arg[]){

        int count=0;
        int result=0;


        System.out.println("result = " + result);

        result = count++;

        System.out.println("result = " + result);

        result = ++count;

        System.out.println("result = " + result);

        result++;

        System.out.println("result = " + result);

        ++result;

        System.out.println("result = " + result);



    }
}
